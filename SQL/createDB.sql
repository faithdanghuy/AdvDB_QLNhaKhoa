    TRANGTHAIDIEUTRI INT, -- 0: KẾ HOẠCH (XANH DƯƠNG),
        -- 1: ĐÃ HOÀN THÀNH (XANH LÁ), 2: ĐÃ HỦY (VÀNG)
    PHIDIEUTRI INT,
    LOAITHANHTOAN INT, -- 0: TIỀN MẶT, 1: ONLINE
