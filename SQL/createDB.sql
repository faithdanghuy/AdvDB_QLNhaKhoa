    IDTROKHAM VARCHAR(5),
    IDLIEUTRINH VARCHAR(5),
    IDTHANHTOAN VARCHAR(5),

    CONSTRAINT PK_KEHOACHDIEUTRI
    PRIMARY KEY(IDHOSO, IDKEHOACH),

    CONSTRAINT FK_KEHOACHDIEUTRI_HOSOBENHNHAN
    FOREIGN KEY(IDHOSO)
    REFERENCES HOSOBENHNHAN,

