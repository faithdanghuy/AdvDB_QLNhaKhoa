    FOREIGN KEY(IDNHASI)
    REFERENCES TAIKHOAN,
    CONSTRAINT FK_KEHOACHDIEUTRI_TAIKHOAN_IDTROKHAM
    FOREIGN KEY(IDTROKHAM)
    REFERENCES TAIKHOAN,

    CONSTRAINT FK_KEHOACHDIEUTRI_LIEUTRINH
